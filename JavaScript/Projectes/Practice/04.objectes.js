

// objects are real-world entity or a thing with its properties (attributes) and behaviors (methods).
// It is a common practice to declare objects with the const keyword.
// The values are written as name:value pairs (name and value separated by a colon).



// create object and print it's property
const car = {type:"Fiat", model:"500", color:"white"};

console.log("Car type is : "+car.type);


// Spaces and line breaks are not important. An object definition can span multiple lines:

const person = {
    firstName: "John",
    lastName: "Doe",
    age: 50,
    eyeColor: "blue"
  };

  console.log("Age of person : "+person.age);  // objectName.propertyName
